      });

      vacancy.addEventListener("click", function(event) {
        event.preventDefault();
        popup_vacancy.classList.add("modal-content-show");
        modal_bg.classList.add("modal-content-show");
      });


      modal_bg.addEventListener("click", function(event) {
        event.preventDefault;
        popup_vacancy.classList.remove("modal-content-show");
        modal_bg.classList.remove("modal-content-show");
      });


      window.addEventListener("keydown", function(event){
        if(event.keyCode == 27) {
          if(popup_vacancy.classList.contains("modal-content-show")){
            popup_vacancy.classList.remove("modal-content-show");
          }
        }
      });


      window.addEventListener("keydown", function(event){
        if(event.keyCode == 27) {
          if(modal_bg.classList.contains("modal-content-show")){
            modal_bg.classList.remove("modal-content-show");
          }
        }
      });
    </script>

















